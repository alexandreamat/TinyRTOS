#ifndef __BASE_REGISTERS_H__
#define __BASE_REGISTERS_H__

#define PERIPHERAL_BASE (0xFE000000)

// Auxiliaries: UART1, SPI1 & SPI2
#define AUX_BASE (PERIPHERAL_BASE + 0x215000)

// BSC
#define BSC0_BASE (PERIPHERAL_BASE + 0x205000)
#define BSC1_BASE (PERIPHERAL_BASE + 0x804000)
#define BSC3_BASE (PERIPHERAL_BASE + 0x205600)
#define BSC4_BASE (PERIPHERAL_BASE + 0x205800)
#define BSC5_BASE (PERIPHERAL_BASE + 0x205A80)
#define BSC6_BASE (PERIPHERAL_BASE + 0x205C00)

// DMA Controller
#define DMA0_BASE (PERIPHERAL_BASE + 0x007000)

// General Purpose I/O (GPIO)
#define GP_BASE (PERIPHERAL_BASE + 0x200000)

// Interrupts
#define GIC_400_BASE (0xFF840000)
#define ARM_LOCAL_BASE (0xFF800000)
#define ARMC_BASE (PERIPHERAL_BASE + 0x00B000)

// PCM / I2S Audio
#define PCM_BASE (PERIPHERAL_BASE + 203000)

// Pulse Width Modulator
#define PWM0_BASE (PERIPHERAL_BASE + 0x20C000)
#define PWM1_BASE (PERIPHERAL_BASE + 0x20C800)

// SPI
#define SPI0_BASE (PERIPHERAL_BASE + 0x204000)
#define SPI3_BASE (PERIPHERAL_BASE + 0x204600)
#define SPI4_BASE (PERIPHERAL_BASE + 0x204800)
#define SPI5_BASE (PERIPHERAL_BASE + 0x204A00)
#define SPI6_BASE (PERIPHERAL_BASE + 0x204C00)

// System Timer
#define SYS_TIMERS_BASE (PERIPHERAL_BASE + 0x003000)

// UART
#define PL011_UART0_BASE (PERIPHERAL_BASE + 0x201000)
#define PL011_UART2_BASE (PERIPHERAL_BASE + 0x201400)
#define PL011_UART3_BASE (PERIPHERAL_BASE + 0x201600)
#define PL011_UART4_BASE (PERIPHERAL_BASE + 0x201800)
#define PL011_UART5_BASE (PERIPHERAL_BASE + 0x201A00)

// Timer (ARM side)
#define ARM_LOCAL (0xFF800000)

#endif  // __BASE_REGISTERS_H__